package model;

public class ContenutoPreferito 
{
	private int idPreferito;
	private String username;
	private int idContenuto;
	
	public int getIdPreferito() {
		return idPreferito;
	}
	
	public void setIdPreferito(int idPreferito) {
		this.idPreferito = idPreferito;
	}
	
	public String getUsername() {
		return username;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public int getIdContenuto() {
		return idContenuto;
	}
	
	public void setIdContenuto(int idContenuto) {
		this.idContenuto = idContenuto;
	}
}